const normalizedWordAllowlist = new Set(germanWordAllowlist.map((word) => normalizeWordTilesWord(word)));

export function normalizeWordTilesWord(word: string): string {
  return word
    .trim()
    .toLocaleUpperCase("de-DE")
    .replace(/ß/g, "SS")
    .replace(/[^A-ZÄÖÜ]/g, "");
}

export function isWordTilesAllowedWord(word: string): boolean {
  const normalized = normalizeWordTilesWord(word);

  if (normalized.length < 2) {
    return false;
  }

  return normalizedWordAllowlist.has(normalized);
}
